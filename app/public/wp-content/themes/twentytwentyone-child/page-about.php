<h4>Eric Reguly is the European Bureau Chief for The Globe and Mail, based in Rome, Italy. Since 2007, when he moved to Europe, he has primarily covered economic and financial stories, ranging from the euro zone crisis and the bank bailouts to the rise and fall of Russia's oligarchs and the merger of Fiat and Chrysler. He also covered the Arab Spring in Tunisia, the Athens riots, the London and Sochi Olympics, the 2013 papal conclave and several national elections.

</h4>

<h4>Before his European assignment, Eric wrote the paper's main business column from Toronto. He also co-hosted a daily business programme on BNN, formerly Report on Business Television, and contributed to several magazines, including Time Canada. In Europe, he is a regular guest on Canadian and American radio programs, contributes to CTV News and makes speeches about business issues. In recent years, he has been a judge for the Prix Italia, an international TV, radio and web competition. He also writes a regular column in The Globe's Report on Business magazine and is a contributor to Newsweek.

</h4>

<h4>Eric has won several awards for his work, including, in 2007, the Hyman Solomon Award for Excellence in Public Policy Journalism. He was co-winner in 2010 of a National Newspaper Award and has twice been an NNA runner-up in the columnist category. In 2010, he won gold in the National Magazine Awards and took silver in 2013. He has contributed to several books, including Orange Alert, a remembrance of the 9/11 terrorist attacks, and Beautiful Destruction, an essay and photo book on the Alberta oil sands.

</h4>

<h4>
He joined The Globe and Mail in late 1997. He has an Honours Bachelor of Arts degree in English and French Literature and a Masters in Journalism, both from the University of Western Ontario. Previous employers include The Times of London, the Financial Post in its New York and London bureaus, the Financial Times of Canada and Alberta Report magazine.
</h4>

<h4>Eric was born in Vancouver, grew up in Toronto, Washington and Rome and has Canadian and Italian citizenship.

